void PrintBootTimer()
{
	lcdSetCursor(0, lcdGetHeight() - font.Height);
	printf("Booting in %d...\r\n", boot_timer);
}

void LoadApp()
{
	lcdSetCursor(0, font.Height * 10);

	if (exist_update)
	{
	   ETX_SD_EX_ sd_ex = check_update_firmware_SD_card(file_name);
	   if (sd_ex != 0)
	   {
		   printf("Firmware update error (%u)\r\n", sd_ex);
	   }
	   else
	   {
		   printf("Firmware updated.\r\n");
	   }
	}

	GoToApplication();
}

static void GoToApplication(void)
{
  printf("Switching to app %s\r\n", file_name);
  HAL_Delay(switch_delay);

  void (*app_reset_handler)(void) = (void*)(*((volatile uint32_t*) (ETX_APP_FLASH_ADDR + 4U)));

   /* Reset the Clock */

  HAL_RCC_DeInit();
  HAL_DeInit();
  __set_MSP(*(volatile uint32_t*) ETX_APP_FLASH_ADDR);

  SysTick->CTRL = 0;
  SysTick->LOAD = 0;
  SysTick->VAL = 0;

  /* Jump to application */
  app_reset_handler();    //call the app reset handler
}


void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	static uint8_t file_number = 0;

	if (GPIO_Pin == KEY0_Pin)
	{
		boot_timer = 0;
	}

	if (GPIO_Pin == KEY1_Pin && !debounce_flag)
	{
		exist_update = true;
		file_number = (file_number + 1) % num_files;
		boot_timer = boot_timer_max;
		PrintBootTimer();

		sprintf(file_name, "bin/%s", file_names[file_number]);

		lcdSetCursor(0, font.Height * 9);
		lcdPrintf("                          \r");
		printf("FW: %s\r\n", file_names[file_number]);

		debounce_flag = true;
		HAL_TIM_Base_Start_IT(&htim6);
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if (htim == &htim6)
	{
		HAL_TIM_Base_Stop_IT(&htim6);
		debounce_flag = false;
	}
	else if (htim == &htim7)
	{
		--boot_timer;
		PrintBootTimer();
	}
}

